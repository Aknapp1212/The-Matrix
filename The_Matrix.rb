#Simulates an encounter between you and the agents
#returns 1 if you win
# -1 if you lose
# 0 if you escape
def the_fight(action, pp_level, ap_level)
	final_outcome = 0
  the_ap_level = ap_level
  the_pp_level = pp_level
	case action
	when "fight"
		if the_ap_level > the_pp_level
			diff = the_ap_level - the_pp_level
			i = 2
			p = 7
			done = false
			while !done
				if diff <= i
					num = rand(1..10)
					if num > p
						final_outcome = -1
						done = true
            separator
						puts "Sorry, looks like he was stronger than you and beat you."
					else
						final_outcome = 1
						done = true
            separator
            enhanced_strings("You win")
						puts "Good job, you won the fight even though he was stronger than you. Your power level has increased. Find out
						how strong are you with the oracle. (Hit enter)"
            pause = gets.chomp
            separator
					end
				end
				i+=2
				p-=1.5
			end
		elsif the_pp_level > the_ap_level
			diff = the_pp_level - the_ap_level
			i = 2
			p = 7
			done = false
			while !done
				if diff <= i
					num = rand(1..10)
					if num > p
						final_outcome = 1
						done = true
            separator
            enhanced_strings("You win")
						puts "Alright, you taught that guy a lesson, you were stronger than him though."
					else
						final_outcome = -1
						done = true
            separator
						puts "He gave you a whooping, maybe you should run next time."
					end
				end
				i+=2
				p-=1
			end
		else
			the_pp_level == the_ap_level
			num = rand(1..2)
			if num == 1
				final_outcome = 1
        separator
        enhanced_strings("You win")
				puts "Equal levels, and you beat him, nice job bro."
			else
				final_outcome = -1
        separator
				puts "Sorry body, that was a close fight though."
			end

		end
	when "run"
		chances = rand(1..10)
		if chances > 4
			final_outcome = 0
      separator
			puts "That was fast, now you see me, now you don't."
		else
			final_outcome = -1
      separator
			puts "Maybe you should've fought, you weren't fast enough and now you're dead."
		end
	when "fly"
		final_outcome = -1 #"Fool you can't fly"
    separator
		puts "What are you thinking, you can't fly, you just got swatted like a fly"
	when "talk"
		chances = rand(1..10)
		if chances > 8
			final_outcome = 0
      separator
			puts "Wow how about those persuasive skills, you really talked this guy into not kicking you in the face."
		else
			final_outcome = -1
      separator
			puts "What are you doing, trying to talk to these guys, they don't care about your problems."
		end
	end
	return final_outcome
end

