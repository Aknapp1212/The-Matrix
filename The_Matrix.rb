#returns the player's choice
def chosen_op(see_him, ap_level)
	good_ans = false
  the_ap_level = ap_level
  see_agent_smith = see_him
	while !good_ans
		if see_agent_smith
			puts "Oh damn, it's Agent Smith. You can Run, Fight, Fly out of here, or try talking your way out of this one, 
			although I wouldn't recommended it with this guy.(Please type in Run, Fight, Fly, or Talk)"
			answer = gets.chomp.downcase
		else
        separator
		    puts "You see an agent, he's difficulty level is #{the_ap_level}/10xp. You can Run, Fight, Fly out of here, or Convince the agent that you're a nice
			guy.(Please type in Run, Fight, Fly, or Talk)"
			answer = gets.chomp.downcase
		end
		if answer == "run" || answer == "fight" || answer == "fly" || answer == "talk"
			good_ans = true
			final_ans = answer
		else
			puts "You cant do that, choose one of the options."
		end
	end
	return final_ans
end

